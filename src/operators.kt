fun main() {

    //Arithmetic operators
    var x = 10
    var y = 3


    println(x+y)
    println(x-y)
    println(x*y)
    println(x/y)
    println(x%y)  //Modulus returns remeinder

    //Comparison operators
    var num1 = 45
    var num2 = 30
    println(num1 < num2)
    println(num1 > num2)
    println(num1 <= num2)
    println(num1 >= num2)
    println(num1 == num2) //Equal to
    println(num1 != num2) //Not equal to

    //Assignment operators
    var z = 56
    println(z)

    var number = 10
    number += 2
    println(number)

    // Logic Operators - &&, ||, ! (and or not)
    println(67 > 12 && 80 < 6)
    println(67 > 12 || 80 < 6)
    println(!(67 > 12 || 80 < 6))


















}